    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int row = Table.getSelectedRow();
        if (row==-1)
        {
            JOptionPane.showMessageDialog(BanAn.this, "Vui lòng chọn hàng cần xoá", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
        else
        {
            int cofirm=JOptionPane.showConfirmDialog(BanAn.this, "Bạn có muốn xoá không ?");
            if (cofirm==JOptionPane.YES_OPTION)
            {
                String IdTable=String.valueOf(Table.getValueAt(row, 0));
                tableService.deleteTable(IdTable);
                defaultTableModel.setRowCount(0);
                setTableData(tableService.getAllTable());
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
            table.setIdTable(IdTableTextField.getText());
            tableService.addTable(table);
            btnAdd.setEnabled(true);
            btnSave.setEnabled(false);
            btnDelete.setEnabled(false);
            defaultTableModel.setRowCount(0);
            setTableData(tableService.getAllTable());
    }//GEN-LAST:event_btnSaveActionPerformed

