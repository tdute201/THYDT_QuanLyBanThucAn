    private void setTableData(List<Table> table)
    { 
        int i =1;
        for (Table tab : table)
        {
            if(tab.getIdBan().contains("B"))
            defaultTableModel.addRow(new Object[]{tab.getIdBan(),"Bàn "+ i++});
        }
    }
    private String LoadIdTable()
    {
        Connection con=connection.getJDBCConnection();
            String IdTable="";
            String sql="select ID=dbo.fnAUTO_ID('B0')";
            PreparedStatement pstm;
            try {
                pstm = con.prepareStatement(sql);
                ResultSet rs = pstm.executeQuery();
                while(rs.next()){
                IdTable=rs.getString("ID");
                }
            } catch (SQLException ex) {
                Logger.getLogger(BanAn.class.getName()).log(Level.SEVERE, null, ex);
            }
            return IdTable;
    }
