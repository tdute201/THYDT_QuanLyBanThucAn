    private void tbVCMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbVCMouseClicked
        int row = tbVC.getSelectedRow();
        int phanTram = (int) tbVC.getValueAt(row, 3);
        tTien = Integer.parseInt(this.thanhTien.getText());
        tGiam = tTien*phanTram*1.0/100;
        tong = tTien-tGiam;
        tienGiam.setText(""+tGiam);
        tongTien.setText(""+(tong));
    }//GEN-LAST:event_tbVCMouseClicked

    private void btThanhToanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btThanhToanActionPerformed
        Xuat xuatfile = new Xuat();
        HDonn hd = serviceHoaDo.getHoaDon(idDH);
        xuatfile.xuatFile(hd.getNgayTao(),listCTDH,tTien,tGiam,tong);
        serviceHoaDo.Update(idDH);
        UserDao us = new UserDao();
        us.IsOrdered_k(idB);
        
        try {
            int row = tbVC.getSelectedRow();
            if(row!=1)
            {
                String idVC = (String) tbVC.getValueAt(row, 0);
                int sl = (int) tbVC.getValueAt(row, 2);
                serviceHoaDo.Update_HD_VC(idDH, idVC);
                serviceHoaDo.Update_sl_VC(idVC, sl);
            }
        } catch (Exception e) {
        }
        JOptionPane.showMessageDialog(rootPane, "Thanh toán thành công!");
        
        gm.setVisible(false);
        this.dispose();
        new BanAn_MangVe().setVisible(true);
    }//GEN-LAST:event_btThanhToanActionPerformed

