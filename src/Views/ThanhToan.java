    public void setTableVC(List<Voucher> list){
            for (Voucher vc : list) {
                defaultTableModel.addRow(new Object[]{vc.getIdVoucher(),vc.getTenVoucher(),vc.getSoLuong(),vc.getPhanTram()});
            }
        }
    public void setTableCTDH(List<ChiTietDonHang> list){
            for (ChiTietDonHang ct : list) {
                String tenSP ="";
                for(SanPham sp : us.getAllSP())
                    if(ct.getIdSP() == null ? sp.getId() == null : ct.getIdSP().equals(sp.getId()))
                        tenSP = sp.getTen();
                tableModel.addRow(new Object[]{tenSP,ct.getSLMua(),ct.getGiaBan()});
            }
        }
