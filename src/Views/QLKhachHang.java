    public void setTableKH(List<KHHang> list){
        
        for (KHHang object : list) {
            
            defaultTableModel.addRow(new Object[]{object.getIdKH(),object.getTenKH(),object.getSdt(), object.getDiaChi(), object.getLoaiKH()});
        }
    }
    public void setTableChiTiet(List<HDonn> list){
        serviceHoaDo = new ServiceHoaDo();
        for (HDonn hDonn : list) {
            int tongTien = serviceHoaDo.tongTien(hDonn.getIdHD());
            tableModel.addRow(new Object[]{hDonn.getIdHD(),hDonn.getNgayTao(),tongTien});
        }
    }
     public void setTableCTHD(List<CTHD> list){
        serviceSP = new UserServices();
        for (CTHD cthd : list) {
            String id = cthd.getIdSP();
            String tenSP="", kichCo="";
            for (SanPham sp : serviceSP.getAllSP()) {
                if(id == null ? sp.getId() == null : id.equals(sp.getId()))
                {
                    tenSP = sp.getTen();
                    kichCo = sp.getDinhLuong();
                }
            }
            model.addRow(new Object[]{tenSP,kichCo,cthd.getSoLuong(),cthd.getDonGia()});
        }
    }
     public boolean kiemTraSDT(String sdt){
         for (KHHang kHHang : serviceKH.getKh()) {
             if(kHHang.getSdt() == null ? sdt == null : kHHang.getSdt().equals(sdt))
                 return true;
         }
         return false;
     }
     public boolean kiemTraSDT1(String sdt, String idKH){
         for (KHHang kHHang : serviceKH.getKh()) {
             if(kHHang.getIdKH() == null ? idKH != null : !kHHang.getIdKH().equals(idKH))
                if(kHHang.getSdt() == null ? sdt == null : kHHang.getSdt().equals(sdt))
                    return true;
         }
         return false;
     }
