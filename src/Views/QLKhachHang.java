        String idKH = (String) tbKH.getValueAt(row, 0);
        khang.setIdKH(idKH);
        if("".equals(tfTen.getText())||"".equals(tfDiaChi.getText())||"".equals(tfSdt.getText()))
            JOptionPane.showMessageDialog(rootPane, "Tên, Số điện thoại và Địa chỉ không được để trống");
        else
            if(kiemTraSDT1(tfSdt.getText(),idKH))
                JOptionPane.showMessageDialog(rootPane, "Số điện thoại đã tồn tại!");
            else
            {
                serviceKH.Update(khang);
                defaultTableModel.setRowCount(0);
                setTableKH(serviceKH.getKh());
            }
        
        
