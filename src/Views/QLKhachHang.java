    private void btThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btThemActionPerformed
        if("".equals(tfTen.getText())||"".equals(tfDiaChi.getText())||"".equals(tfSdt.getText()))
            JOptionPane.showMessageDialog(rootPane, "Tên, Số điện thoại và Địa chỉ không được để trống");
        else
        {
            KHHang khang = new KHHang();
            if(!kiemTraSo.isNumeric(tfSdt.getText()))
            {
                JOptionPane.showMessageDialog(rootPane, "Số điện thoại không hợp lệ");
                tfSdt.setText("");
            }
            else
            {
                if(tfSdt.getText().length()<10 || tfSdt.getText().length()>11)
                    JOptionPane.showMessageDialog(rootPane, "Số điện thoại phải có 10 hoặc 11 số");
                else
                {
                    khang.setSdt(tfSdt.getText());
                    String tenLoai = (String) cbLoaiKH.getSelectedItem();
                    khang.setTenKH(tfTen.getText());
                    khang.setDiaChi(tfDiaChi.getText());
                    khang.setLoaiKH(tenLoai);
                    khang.setIdKH(serviceKH.NewIDKH());
                    if(kiemTraSDT(tfSdt.getText()))
                        JOptionPane.showMessageDialog(rootPane, "Số điện thoại đã tồn tại!");
                    else
                    {
                        serviceKH.Insert(khang);
                        defaultTableModel.setRowCount(0);
                        setTableKH(serviceKH.getKh());
                    }
                    
                }
            }
            
            
        }
        
    }//GEN-LAST:event_btThemActionPerformed

